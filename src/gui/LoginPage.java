    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        var id=user_name.getText();
        String pass= password.getText();
        try
        {
            ResultSet rs=db.DbConnector.st.executeQuery("select * from login_info where username='"+id+"' and password='"+pass+"'");
            if(rs.next())
            {
                int conf = JOptionPane.showConfirmDialog(null, "Do You Want To Display The Username and Password", "Display Confirmation", JOptionPane.YES_NO_OPTION);
                if(conf==JOptionPane.YES_OPTION)
                {
                    JOptionPane.showMessageDialog(null,"Your username is "+id+" \n Your password is "+pass);
                }
                new SpendingTracker().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please enter a valid Username and Password");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

