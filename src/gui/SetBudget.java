    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try
        {
            java.util.Date dt1=d1.getDate();
            java.util.Date dt2=d2.getDate();
            String s1=b_amt.getText();
            if(dt1!=null && dt2!=null && !s1.equals(""))
            {
                int b_amount=Integer.parseInt(s1);
                java.sql.Date date1=new java.sql.Date(dt1.getTime());
                java.sql.Date date2=new java.sql.Date(dt2.getTime());
                if(dt1.getTime()<dt2.getTime())
                {
                    db.DbConnector.st.executeUpdate("insert into budget (b_from_date,b_to_date,b_amount) values('"+date1+"','"+date2+"','"+b_amount+"')");
                    JOptionPane.showMessageDialog(null,"Budget Set Successfully");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Please Check the Dates");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please Fill All Details");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }

