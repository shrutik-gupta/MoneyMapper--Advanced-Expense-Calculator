    private void getEntries()
    {
        try
        {
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)ctable.getModel();
            int rc =dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0);
            }
            ResultSet rs=db.DbConnector.st.executeQuery("select * from category_info");
            int sno=0;
            while(rs.next())
            {
                String category = rs.getString("category");
                /*
                Object o[] =
                {
                    ++sno,category
                };
                dtm .addRow(o);
                */
                java.util.Vector row=new java.util.Vector();
                row.add(++sno);
                row.add(category);
                dtm.addRow(row);
            }
        }
        catch(Exception  ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
