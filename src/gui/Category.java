    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        int ri =ctable.getSelectedRow();
        if(ri!=-1)
        {
            int conf = JOptionPane.showConfirmDialog(null, "Do You Really Want To Delete This Category", "Deleting Confirmation", JOptionPane.YES_NO_OPTION);
            if(conf==JOptionPane.YES_OPTION)
            {
                String category =(String)ctable.getValueAt(ri, 1);
                try
                {
                    db.DbConnector.st.executeUpdate("delete from category_info where category='"+category+"'");
                    JOptionPane.showMessageDialog(null, "Category Deleted Successfully");
                    getEntries();
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        try
        {
            String category =t.getText();
            if (!category.equals(""))
            {
                db.DbConnector.st.executeUpdate ("insert into category_info values('"+category+"')");
                JOptionPane.showMessageDialog(null, "Category Added Successfully");
                getEntries();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Please Enter A Valid Category");
            }
        }
        catch(SQLIntegrityConstraintViolationException ex)
        {
            JOptionPane.showMessageDialog(null, "Category Already Exists");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

